def get_lang() -> str:
    try:
        lang = lang_path().read_text().strip()
        return lang if lang in STRINGS else "en"
    except FileNotFoundError:
        return "en"


def t(message_key: str, **kwargs: object) -> str:
    lang = get_lang()
    if message_key not in STRINGS.get(lang, {}):
        raise KeyError(f"Missing i18n key: {message_key}")
    return STRINGS[lang][message_key].format(**kwargs)
